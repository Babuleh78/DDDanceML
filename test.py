async def test():
    async with httpx.AsyncClient() as client:
        for s in STRATEGIES:
            print(f"\n{'='*50}")
            print(f"Стратегия: {s['name']}")
            print(f"temperature={s['temperature']}, max_tokens={s['max_tokens']}")
            print("-" * 50)

            payload = {
                "model": "local",
                "stream": False,
                "temperature": s["temperature"],
                "top_p": s.get("top_p", 0.9),
                "max_tokens": s["max_tokens"],
                "messages": [
                    {"role": "system", "content": s["system"]},
                    {"role": "user", "content": s["user"]},
                ],
            }
            r = await client.post(f"{GPU_SERVER_URL}/api/chat", json=payload, timeout=60)
            result = r.json()["message"]["content"].strip()
            print(f"Ответ: {result}")
            print(f"Слов: {len(result.split())}")

asyncio.run(test())